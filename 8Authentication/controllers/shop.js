exports.getInvoice = (req, res, next) => {
  const orderId = req.params.orderId;
  Order.findByPk(orderId, {include: 'products'})
  .then(order => {
    if(!order) {
      return next(new Error('No order found!'));
    }
    if(order.userId.toString() !== req.user.id.toString()) {
      return next(new Error('Unauthorized'));
    }
    const invoiceName = "Invoice-" + orderId + ".pdf";
    const invoicePath = path.join('data' , 'invoices' , invoiceName);

    const pdfDoc = new PDFDocument();
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', 'inline; filename="' + invoiceName + '"');
    pdfDoc.pipe(fs.createWriteStream(invoicePath));
    pdfDoc.pipe(res);

    pdfDoc.fontSize(30).text('Invoice', {
      underline: true
    });
    pdfDoc.text('____________________________\n\n');
    let totalPrice = 0;
    order.products.forEach(prod => {
      totalPrice += prod.orderItem.quantity * prod.price;
      pdfDoc.fontSize(14).text(prod.title + ' - ' + prod.orderItem.quantity + ' x ' + 'Rs. ' + prod.price);
    });
    pdfDoc.fontSize(30).text('____________________________\n\n');
    pdfDoc.fontSize(18).text('Total Price:  Rs. ' + totalPrice);

    pdfDoc.end();
  })
  .catch(err => next(err));
};